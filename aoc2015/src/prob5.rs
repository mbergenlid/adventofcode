fn is_nice(s: &str) -> bool {
    let vowels = s.chars().filter(|c| ['a', 'e', 'i', 'o', 'u'].contains(c)).count();
    if vowels < 3 {
        return false;
    }
    let mut last_char = s.chars().next().unwrap();
    let mut has_double = false;
    for c in s.chars().skip(1) {
        if c == last_char {
            has_double = true;
        }
        if last_char == 'a' && c == 'b' {
            return false;
        }
        if last_char == 'c' && c == 'd' {
            return false;
        }
        if last_char == 'p' && c == 'q' {
            return false;
        }
        if last_char == 'x' && c == 'y' {
            return false;
        }
        last_char = c;
    }
    has_double
}

fn is_nice_2(s: &str) -> bool {
    let pairs:Vec<_> = s.chars().zip(s.chars().skip(1)).collect();
    let mut repeats = false;
    for (i, pair) in pairs.iter().enumerate() {
        if pairs.iter().skip(i+2).any(|p| p == pair) {
            repeats = true;
            break;
        }
    }
    let mut duplicate = false;
    for (c1, c2) in s.chars().zip(s.chars().skip(2)) {
        if c1 == c2 {
            duplicate = true;
            break;
        }
    }
    repeats && duplicate
}

